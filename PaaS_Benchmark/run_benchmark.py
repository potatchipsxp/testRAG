#!/usr/bin/env python3
"""
run_benchmark.py

Benchmark runner. Iterates over the 25 PaaS incidents in BENCHMARK_CASES,
calls diagnose() on each, and writes a results JSON file.

This is the script you actually run to produce a benchmark output. The model
configuration (which Qwen variant, temperatures, etc.) lives in
diagnostic_agent.py — to swap models for a comparison run, edit the CONFIG
constants there, then run this script.

The output filename is auto-generated by diagnostic_agent.OUTPUT_FILE so it
encodes the model combination and won't overwrite results from other runs.

Usage:
    python run_benchmark.py
    python run_benchmark.py --tier 3       # only Tier 3 incidents
    python run_benchmark.py --limit 5      # only first 5 incidents
    python run_benchmark.py --output my_run.json   # override filename

For a quick wiring smoke test instead of a full benchmark:
    python diagnostic_agent.py
"""

import argparse
import sys

from benchmark_incidents import BENCHMARK_CASES
from diagnostic_agent import (
    build_diagnostic_agent,
    diagnose,
    save_results,
    OUTPUT_FILE,
    DIAGNOSTIC_MODEL,
    DIAGNOSTIC_BACKEND,
    SQL_MODEL,
    SQL_BACKEND,
    DOC_MODEL,
    DOC_BACKEND,
    VERBOSE,
)


def main():
    ap = argparse.ArgumentParser(description=__doc__.split("\n")[2])
    ap.add_argument("--tier", type=int, choices=[1, 2, 3],
                    help="run only incidents at this tier (default: all)")
    ap.add_argument("--limit", type=int,
                    help="run only the first N incidents (default: all)")
    ap.add_argument("--output", default=OUTPUT_FILE,
                    help=f"output filename (default: {OUTPUT_FILE})")
    args = ap.parse_args()

    # Filter cases based on flags
    cases = BENCHMARK_CASES
    if args.tier is not None:
        cases = [c for c in cases if c.get("tier") == args.tier]
        print(f"Filtering to Tier {args.tier} incidents only.")
    if args.limit is not None:
        cases = cases[:args.limit]
        print(f"Limiting to first {args.limit} incident(s).")

    if not cases:
        sys.exit("No incidents matched the filter — nothing to run.")

    # Print run header
    print("=" * 70)
    print("BENCHMARK RUN")
    print("=" * 70)
    print(f"Diagnostic model : {DIAGNOSTIC_MODEL} ({DIAGNOSTIC_BACKEND})")
    print(f"SQL model        : {SQL_MODEL} ({SQL_BACKEND})")
    print(f"Doc model        : {DOC_MODEL} ({DOC_BACKEND})")
    print(f"Incidents        : {len(cases)} of {len(BENCHMARK_CASES)}")
    print(f"Output file      : {args.output}")
    print("=" * 70)

    # Build the orchestrator agent once and reuse it across all incidents
    agent, tools, trace = build_diagnostic_agent()

    # Run each incident
    all_results = []
    for case in cases:
        result = diagnose(
            incident_id=case["incident_id"],
            question=case["question"],
            agent=agent,
            trace=trace,
            verbose=VERBOSE,
        )
        all_results.append(result)

    # Save
    save_results(all_results, output_file=args.output)

    print()
    print("=" * 70)
    print(f"Benchmark complete. {len(all_results)} incident(s) processed.")
    print(f"Results saved to: {args.output}")
    print("Next: python Results/evaluate.py")
    print("=" * 70)


if __name__ == "__main__":
    main()
